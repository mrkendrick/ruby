# Basic arithmetic operations in a module
module MathUtils
  def self.add(num1, num2)
    num1 + num2
  end

  def self.subtract(num1, num2)
    num1 - num2
  end

  def self.multiply(num1, num2)
    num1 * num2
  end

  def self.divide(num1, num2)
    num1 / num2
  end

  def self.mod(num1, num2)
    num1 % num2
  end
end
