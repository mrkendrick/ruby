# Loops
# while loop
# for loop
# do while loop
# until loop

# while loop
x = 4

while x >= 1
  puts x
  x -= 1
end

# for loop
for value in 1...10 do
  puts value
end

# do while loop
loop do
  puts 'Charles'

  val = 7

  break if val == 7
end

# until loop
y = 10

until y == 1
  puts y * 10
  y -= 1
end
